/**
 * Represents a point in 2D space.
 */
public class Point {
   /**
    * The x-coordinate of this point.
    */
   double x;

   /**
    * The y-coordinate of this point.
    */
   double y;

   /**
    * Constructs a new point at the origin of the coordinate space (0, 0).
    */
   public Point() {
       this.x = 0;
       this.y = 0;
   }
}
